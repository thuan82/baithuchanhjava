package Getter_Settter;

public class ToaDo {
    private int x;
    private int y;

    //Getter thuộc tính của x
    public int getX()
    {
        return x;
    }

    //Setter thuộc tính của x
    public void setX(int x)
    {
        this.x = x;
    }

    //Getter thuộc tính của y
    public int getY()
    {
        return y;
    }

    //Setter thuộc tính của y
    public void setY(int y)
    {
        this.y = y;
    }
}